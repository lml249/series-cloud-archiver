def _first_cell_value(value: object) -> str:
    values = _split_paths_cell(value)
    return values[0] if values else ""


def _split_paths_cell(value: object) -> List[str]:
    if isinstance(value, list):
        return [str(item) for item in value if str(item)]
    if isinstance(value, str):
        return [part.strip() for part in value.split("|") if part.strip()]
    return []


def _split_cell(value: object) -> List[str]:
    if isinstance(value, list):
        return [str(item) for item in value if str(item)]
    if isinstance(value, str):
        return [part.strip() for part in value.split(";") if part.strip()]
    return []


def _episodes_from_cell(value: object) -> List[int]:
    if isinstance(value, list):
        return _ints(value)
    if not isinstance(value, str):
        return []
    numbers: List[int] = []
    for part in value.split(","):
        token = part.strip()
        if not token:
            continue
        if "-" in token:
            left, right = token.split("-", 1)
            if left.strip().isdigit() and right.strip().isdigit():
                start, end = int(left), int(right)
                if start <= end:
                    numbers.extend(range(start, end + 1))
            continue
        if token.isdigit():
            numbers.append(int(token))
    return sorted(set(numbers))


def _strings(value: object) -> List[str]:
    if isinstance(value, list):
        return [str(item) for item in value if str(item)]
    if isinstance(value, str) and value:
        return [value]
    return []


def _unique_strings(values: Sequence[object]) -> List[str]:
    result: List[str] = []
    seen: set[str] = set()
    for value in values:
        text = str(value or "").strip()
        if text and text not in seen:
            seen.add(text)
            result.append(text)
    return result


def _ints(value: object) -> List[int]:
    if not isinstance(value, list):
        return []
    result: List[int] = []
    for item in value:
        try:
            result.append(int(item))
        except (TypeError, ValueError):
            continue
    return sorted(set(result))


def _safe_prefix(title: str, tmdbid: int, season: int) -> str:
    slug = re.sub(r"[^0-9A-Za-z一-龥]+", "-", f"{title}-{tmdbid}-s{season:02d}").strip("-")
    return slug[-120:] or "finalize-remediation"


def _q(value: object) -> str:
    return shlex.quote(str(value))


def _nested_str(mapping: JsonDict, *keys: str) -> str:
    value: object = mapping
    for key in keys:
        if not isinstance(value, dict):
            return ""
        value = value.get(key)
    return str(value or "")


def _nested_int(mapping: JsonDict, *keys: str) -> int:
    try:
        return int(_nested_str(mapping, *keys) or 0)
    except ValueError:
        return 0


def _looks_like_full_hash(value: str) -> bool:
    return bool(re.match(r"^[a-fA-F0-9]{32,64}$", str(value or "")))


def _local_video_count(hlink: JsonDict, source_rows: Sequence[object]) -> int:
    total = int(hlink.get("video_count") or 0) if isinstance(hlink, dict) else 0
    for row in source_rows:
        if isinstance(row, dict):
            total += int(row.get("video_count") or 0)
    return total


def _qb_expected_title_contains(title: str, matches: Sequence[JsonDict]) -> str:
    folded_title = str(title or "").casefold()
    for match in matches:
        name = str(match.get("name") or "")
        if folded_title and folded_title in name.casefold():
            return title
    for match in matches:
        name = str(match.get("name") or "")
        found = re.split(r"(?i)(?:[._ -]+S\d{1,2}(?:[E._ -]|\b)|[._ -]+\d{4}[._ -])", name, maxsplit=1)[0]
        cleaned = found.strip(" ._-")
        if cleaned:
            return cleaned
    return title


def _render_markdown(report: JsonDict) -> str:
    lines = [
        "# Finalize Remediation Plan",
        "",
        f"- Planned items: `{report.get('planned_items', 0)}`",
        f"- Category counts: `{report.get('category_counts', {})}`",
        "- Safety: readonly plan only; commands are diagnostics/dry-runs and omit write/delete approval flags.",
        "",
        "| Category | Status | TMDB | S | Title | Blockers | Next action |",
        "| --- | --- | ---: | ---: | --- | --- | --- |",
    ]
    for item in report.get("items", []) if isinstance(report.get("items"), list) else []:
        if not isinstance(item, dict):
            continue
        lines.append(
            "| {category} | {status} | {tmdbid} | {season} | {title} | {blockers} | {next_action} |".format(
                category=_escape_cell(str(item.get("category") or "")),
                status=_escape_cell(str(item.get("finalize_status") or "")),
                tmdbid=item.get("tmdbid") or "",
                season=item.get("season") or "",
                title=_escape_cell(str(item.get("title") or "")),
                blockers=_escape_cell("; ".join(_strings(item.get("blockers")))),
                next_action=_escape_cell(str(item.get("next_action") or "")),
            )
        )
    return "\n".join(lines)


