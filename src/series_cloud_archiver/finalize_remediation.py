def _cloud_title_path(path: str) -> str:
    clean = str(path or "").rstrip("/")
    if re.search(r"(?i)/(?:Season\s*0?\d+|第\s*\d+\s*季)$", clean):
        return clean.rsplit("/", 1)[0]
