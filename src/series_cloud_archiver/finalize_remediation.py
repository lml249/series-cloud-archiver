def _remediation_row(
    review_item: JsonDict,
    finalize_item: JsonDict,
    *,
    env_file: str,
    cloud_media_storage: str,
    timeout: int,
) -> JsonDict:
    blockers = _strings(finalize_item.get("blockers")) or _split_cell(review_item.get("finalize_blockers")) or _split_cell(review_item.get("reason_summary"))
    category = _category(str(finalize_item.get("status") or review_item.get("finalize_status") or ""), blockers)
    title = str(review_item.get("title") or finalize_item.get("title") or "")
    tmdbid = int(review_item.get("tmdbid") or finalize_item.get("tmdbid") or 0)
    season = int(review_item.get("season") or finalize_item.get("season") or 0)
    expected_count = int(review_item.get("expected_episode_count") or finalize_item.get("expected_episode_count") or 0)
    expected_min, expected_max = _episode_range(review_item, finalize_item, expected_count)
    strm_root = str(finalize_item.get("strm_root") or review_item.get("strm_root") or "")
    cloud_season_path = str(finalize_item.get("cloud_season_path") or finalize_item.get("required_target_prefix") or review_item.get("cloud_media_path") or "")
    cloud_title_path = str(finalize_item.get("cloud_title_path") or _cloud_title_path(cloud_season_path) or "")
    hlink_root = str(finalize_item.get("hlink_root") or _first_cell_value(review_item.get("source_paths")) or "")
    source_paths = _strings(finalize_item.get("source_paths")) or _split_paths_cell(review_item.get("source_paths"))
    source_qb_hashes = _strings(finalize_item.get("source_qb_hashes"))
    required_prefix = str(finalize_item.get("required_target_prefix") or finalize_item.get("strm_target_prefix") or cloud_season_path)
    stage_outputs = _stage_outputs(finalize_item)
    commands = _commands(
        category=category,
        title=title,
        tmdbid=tmdbid,
        season=season,
        expected_count=expected_count,
        expected_min=expected_min,
        expected_max=expected_max,
        strm_root=strm_root,
        cloud_season_path=cloud_season_path,
        cloud_title_path=cloud_title_path,
        hlink_root=hlink_root,
        source_paths=source_paths,
        source_qb_hashes=source_qb_hashes,
        required_prefix=required_prefix,
        env_file=env_file,
        cloud_media_storage=cloud_media_storage,
        timeout=timeout,
    )
    return {
        "category": category,
        "next_action": _next_action(category),
        "title": title,
        "tmdbid": tmdbid,
        "season": season,
        "finalize_status": str(finalize_item.get("status") or review_item.get("finalize_status") or ""),
        "finalize_last_stage": str(review_item.get("finalize_last_stage") or _last_stage(finalize_item) or ""),
        "blockers": sorted(set(blockers)),
        "expected_episode_count": expected_count,
        "expected_episode_min": expected_min,
        "expected_episode_max": expected_max,
        "strm_root": strm_root,
        "cloud_title_path": cloud_title_path,
        "cloud_season_path": cloud_season_path,
        "hlink_root": hlink_root,
        "source_paths": source_paths,
        "source_qb_hashes": source_qb_hashes,
        "stage_outputs": stage_outputs,
        "commands": commands,
    }


def _run_remediation_command(
    plan_item: JsonDict,
    command_item: JsonDict,
    *,
    item_index: int,
    command_index: int,
    output_base: Path,
    execute_readonly: bool,
    cwd: str,
    process_timeout: int,
    runner: CommandRunner,
) -> JsonDict:
    title = str(plan_item.get("title") or "")
    tmdbid = int(plan_item.get("tmdbid") or 0)
    season = int(plan_item.get("season") or 0)
    stage = str(command_item.get("stage") or "")
    raw_command = str(command_item.get("command") or "")
    output_path = output_base / _command_output_name(title, tmdbid, season, stage, command_index)
    row: JsonDict = {
        "status": "planned",
        "executed": False,
        "category": str(plan_item.get("category") or ""),
        "stage": stage,
        "title": title,
        "tmdbid": tmdbid,
        "season": season,
        "item_index": item_index,
        "command_index": command_index,
        "output": str(output_path),
        "original_command": raw_command,
        "command": "",
        "returncode": None,
        "diagnostic_ok": None,
        "diagnostic_blockers": [],
        "diagnostic_warnings": [],
        "runner_error": "",
        "safety_blockers": [],
    }
    safety = _safe_command_tokens(raw_command, output_path)
    if safety.get("skip_reason"):
        row["status"] = "skipped"
        row["skip_reason"] = str(safety.get("skip_reason") or "")
        row["command"] = safety.get("command", "")
        return row
    if safety.get("blockers"):
        row["status"] = "unsafe_blocked"
        row["safety_blockers"] = safety.get("blockers", [])
        row["command"] = safety.get("command", "")
        return row
    argv = [str(item) for item in safety.get("argv", []) if str(item)]
    row["command"] = safety.get("command", "")
    if not execute_readonly:
        return row

    started = time.time()
    try:
        completed = runner(
            argv,
            cwd=cwd or None,
            text=True,
            capture_output=True,
            timeout=process_timeout,
            env={**os.environ, "PYTHONPATH": _py_path(cwd)},
        )
    except subprocess.TimeoutExpired as exc:
        row["status"] = "timeout"
        row["executed"] = True
        row["runner_error"] = f"timeout_after_{process_timeout}s"
        row["stdout"] = _truncate_output(getattr(exc, "stdout", "") or "")
        row["stderr"] = _truncate_output(getattr(exc, "stderr", "") or "")
        row["duration_seconds"] = round(time.time() - started, 3)
        return row
    except Exception as exc:  # pragma: no cover - exercised via injected runner in tests.
        row["status"] = "runner_error"
        row["executed"] = True
        row["runner_error"] = f"{type(exc).__name__}: {exc}"
        row["duration_seconds"] = round(time.time() - started, 3)
        return row

    row["executed"] = True
    row["returncode"] = int(getattr(completed, "returncode", 0))
    row["stdout"] = _truncate_output(str(getattr(completed, "stdout", "") or ""))
    row["stderr"] = _truncate_output(str(getattr(completed, "stderr", "") or ""))
    row["duration_seconds"] = round(time.time() - started, 3)
    diagnostic = _load_command_output(output_path)
    if diagnostic:
        row["diagnostic_ok"] = bool(diagnostic.get("ok"))
        row["diagnostic_blockers"] = _strings(diagnostic.get("blockers"))
        row["diagnostic_warnings"] = _strings(diagnostic.get("warnings"))
        row["diagnostic_mode"] = str(diagnostic.get("mode") or "")
        row["diagnostic_summary"] = _diagnostic_summary(diagnostic)
    else:
        row["diagnostic_ok"] = None
    row["status"] = "executed" if row["returncode"] == 0 else "diagnostic_failed"
    return row


def _safe_command_tokens(command: str, output_path: Path) -> JsonDict:
    blockers: List[str] = []
    try:
        tokens = shlex.split(command)
    except ValueError as exc:
        return {"blockers": [f"command_parse_failed:{exc}"], "argv": [], "command": command}
    if len(tokens) < 4 or tokens[:3] != ["PYTHONPATH=src", "python3", "-m"]:
        return {"skip_reason": "non_executable_note", "argv": tokens, "command": command}
    if tokens[3] != "series_cloud_archiver":
        blockers.append("unsupported_module")
    if len(tokens) < 5:
        blockers.append("subcommand_required")
        subcommand = ""
    else:
        subcommand = tokens[4]
    if subcommand not in READONLY_REMEDIATION_COMMANDS:
        blockers.append(f"unsupported_subcommand:{subcommand}")
    if any(token.startswith("--approve-") for token in tokens):
        blockers.append("approval_flag_forbidden")
    if "--output" in tokens:
        index = tokens.index("--output")
        if index + 1 >= len(tokens):
            blockers.append("output_value_required")
        else:
            tokens[index + 1] = str(output_path)
    else:
        tokens.extend(["--output", str(output_path)])
    if "--format" in tokens:
        index = tokens.index("--format")
        if index + 1 < len(tokens):
            tokens[index + 1] = "json"
        else:
            blockers.append("format_value_required")
    else:
        tokens.extend(["--format", "json"])
    argv = ["python3", "-m", "series_cloud_archiver"] + tokens[4:]
    return {"blockers": sorted(set(blockers)), "argv": argv, "command": shlex.join(argv)}


def _command_output_name(title: str, tmdbid: int, season: int, stage: str, command_index: int) -> str:
    prefix = _safe_prefix(title, tmdbid, season)
    safe_stage = re.sub(r"[^0-9A-Za-z_-]+", "-", stage).strip("-") or f"command-{command_index + 1:02d}"
    return f"{prefix}-{command_index + 1:02d}-{safe_stage}.json"


def _py_path(cwd: str) -> str:
    return str(Path(cwd or ".") / "src") if cwd else "src"


def _load_command_output(path: Path) -> JsonDict:
    if not path.exists():
        return {}
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return {}
    return payload if isinstance(payload, dict) else {}


def _diagnostic_summary(report: JsonDict) -> JsonDict:
    summary: JsonDict = {}
    if isinstance(report.get("strm"), dict):
        combined = report["strm"].get("combined") if isinstance(report["strm"].get("combined"), dict) else {}
        summary["strm_episode_count"] = combined.get("episode_count")
        summary["strm_episodes"] = combined.get("episodes")
        summary["strm_missing_in_range"] = combined.get("missing_in_range")
    if isinstance(report.get("summary"), dict):
        for key in (
            "video_file_count",
            "episode_count",
            "episodes",
            "missing_in_range",
            "duplicate_episodes",
            "protected_strm_target_count",
            "strm_file_count",
        ):
            if key in report["summary"]:
                summary[key] = report["summary"][key]
    if isinstance(report.get("delete_plan"), dict):
        summary["duplicate_video_count"] = report["delete_plan"].get("duplicate_video_count")
    return summary


def _truncate_output(value: str, limit: int = 4000) -> str:
    if len(value) <= limit:
        return value
    return value[:limit] + "...[TRUNCATED]"


