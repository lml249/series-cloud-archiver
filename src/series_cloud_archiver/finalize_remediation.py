def run_finalize_remediation_plan(
    plan_report: JsonDict,
    *,
    output_dir: str,
    categories: Optional[Sequence[str]] = None,
    stages: Optional[Sequence[str]] = None,
    titles: Optional[Sequence[str]] = None,
    limit: int = 0,
    execute_readonly: bool = False,
    cwd: str = "",
    process_timeout: int = 300,
    command_runner: Optional[CommandRunner] = None,
) -> JsonDict:
    """Run or dry-run readonly commands from a finalize remediation plan.

    This runner is deliberately narrow. It only accepts commands generated by
    build_finalize_remediation_plan, only for a fixed allowlist of readonly
    subcommands, and blocks any approval flag before execution.
    """

    output_base = Path(output_dir)
    output_base.mkdir(parents=True, exist_ok=True)
    category_filter = {str(item) for item in (categories or []) if str(item)}
    stage_filter = {str(item) for item in (stages or []) if str(item)}
    title_filters = [str(item) for item in (titles or []) if str(item)]
    runner = command_runner or subprocess.run
    rows: List[JsonDict] = []
    selected_items = 0

    for item_index, item in enumerate(plan_report.get("items", []) if isinstance(plan_report.get("items"), list) else []):
        if not isinstance(item, dict):
            continue
        category = str(item.get("category") or "")
        title = str(item.get("title") or "")
        if category_filter and category not in category_filter:
            continue
        if title_filters and not any(token in title for token in title_filters):
            continue
        if limit > 0 and selected_items >= limit:
            break
        selected_items += 1
        commands = item.get("commands") if isinstance(item.get("commands"), list) else []
        for command_index, command_item in enumerate(commands):
            if not isinstance(command_item, dict):
                continue
            stage = str(command_item.get("stage") or "")
            if stage_filter and stage not in stage_filter:
                continue
            rows.append(
                _run_remediation_command(
                    item,
                    command_item,
                    item_index=item_index,
                    command_index=command_index,
                    output_base=output_base,
                    execute_readonly=execute_readonly,
                    cwd=cwd,
                    process_timeout=process_timeout,
                    runner=runner,
                )
            )

    status_counts = Counter(str(row.get("status") or "") for row in rows)
    stage_counts = Counter(str(row.get("stage") or "") for row in rows)
    category_counts = Counter(str(row.get("category") or "") for row in rows)
    diagnostic_counts = Counter(str(row.get("diagnostic_ok")) for row in rows if row.get("executed"))
    runner_error_count = sum(1 for row in rows if bool(row.get("runner_error")))
    unsafe_blocked_count = sum(1 for row in rows if row.get("status") == "unsafe_blocked")
    return {
        "mode": "readonly-finalize-remediation-run",
        "source_mode": str(plan_report.get("mode") or ""),
        "ok": runner_error_count == 0 and unsafe_blocked_count == 0,
        "execute_readonly": execute_readonly,
        "selected_items": selected_items,
        "planned_commands": len(rows),
        "executed_commands": sum(1 for row in rows if bool(row.get("executed"))),
        "runner_error_count": runner_error_count,
        "unsafe_blocked_count": unsafe_blocked_count,
        "status_counts": dict(sorted(status_counts.items())),
        "stage_counts": dict(sorted(stage_counts.items())),
        "category_counts": dict(sorted(category_counts.items())),
        "diagnostic_ok_counts": dict(sorted(diagnostic_counts.items())),
        "output_dir": str(output_base),
        "items": rows,
        "settings": {
            "categories": sorted(category_filter),
            "stages": sorted(stage_filter),
            "titles": title_filters,
            "limit": limit,
            "cwd": cwd,
            "process_timeout": process_timeout,
        },
        "safety": (
            "readonly remediation runner only; it executes a fixed allowlist of diagnostic commands, "
            "rewrites report outputs under output_dir, and blocks any approval flag before execution."
        ),
    }


